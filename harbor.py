def tide_list(location, day_name):
    """ Returns a list of tide heights for the given day_name.
    An exception/error is generated if the day_name is unknown.
    """
    location_id = obfuscate(location)
    day_id = obfuscate(day_name)
    location_data = DATA.get(location_id)
    if location_data is None:
        raise Exception(IndexError, 'No tide data for {}...'.format(location))
    else:
        tide_data = location_data.get(day_id)
        if tide_data is None:
            raise Exception(IndexError, 'No tide data for {} at {}...'.format(day_name, location))        
    return tide_data


def safe_launch_height(location):
    """ Returns a tide height of a given harbour, where it is safe 
    to launch a coastguard boat at this height or above. 
    """
    location_id = obfuscate(location)
    tide_height = LAUNCH_HEIGHTS.get(location_id)
    if tide_height is None:
        raise Exception(IndexError, 'No safe tide height for {}...'.format(location))
    return tide_height    



