def obfuscate(item):
    """Takes a string and returns an 10 digit id as a string"""
    if item is None:
        return 0 # empty
    value = ord(item[0]) << 7
    for char in item:
        value = ((int(1000003) * int(value)) & 0xFFFFFFFF) >> 4 ^ ord(char)
    value = value ^ len(item)
    if value == -1:
        value = -2
    return str(value).zfill(10)

